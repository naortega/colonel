.section .text
.global gdt_flush
.type gdt_flush, @function
gdt_flush:
	pushl %ebp
	movl %esp, %ebp
	movl 8(%ebp), %eax
	cli
	lgdt (%eax)
	movw $0x10, %ax
	movw %ax, %ds
	movw %ax, %es
	movw %ax, %fs
	movw %ax, %gs
	movw %ax, %ss
	ljmp $0x0008, $gdt_flush_end
gdt_flush_end:
	popl %ebp
	ret
